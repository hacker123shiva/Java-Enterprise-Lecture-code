
public class Variable_datatype2 {

	public static void main(String[] args) {
 byte a=5;  // range(-128,127) 
 short b=5; //range (-32768,32767)
 int c=5;  //range(-2,147,483,648 , 2,147,483,647)
 long d=5l; // suffix needed to declare long range (-9,223,372,036,854,775,808 , 9,223,372,036,854,775,807)
 float e=5.5f; //suffix f is mandatory by default ral is double
 double f=5.5;
 boolean t=false;
 char ch='a';
 char chh=97;
 
 System.out.println(a+" "+b+" "+c+" "+d+" "+e+" "+f+" "+t+" "+ch+" "+chh);
	}

}
